public class ternaryOperator {
    public static void main(String[] args) {
        int a = 23;
        int b = 76;
        String W = (a == b) ? "they are equal" : "they are not equal";
        System.out.println(W);

        System.out.println((a != b) ? "True" : "False" );

        System.out.println((a == b) ? 0 : 1);
    }
}
